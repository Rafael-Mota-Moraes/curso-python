"""
Nós temos 2 tipos de dados numéricos
- Os inteiros
    1, 2, 3 ... 100, -1, -2, -3 ... -100

- Os reais ou de ponto flutante
    1.5 === 1,5
    1.5, 2.23, 3.55555
"""

inteiro = 10
print(inteiro, "é um número inteiro")
print("A variável inteiro é do tipo:", type(inteiro))

flutuante = 10.3456
print(flutuante, "é um número de ponto flutuante")
print("A variável flutuante é do tipo:", type(flutuante))