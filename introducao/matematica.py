"""
+ - / *

// - divisão por inteiro
** - exponenciação
"""

num1 = 5
num2 = 3
soma = num1 + num2
print(num1, "+", num2, "=", soma)

subtracao = num1 - num2
print(num1, "-", num2, "=", subtracao)

divisao = num1 / num2
print(num1, "/", num2, "=", divisao)

multiplicacao = num1 * num2
print(num1, "*", num2, "=", multiplicacao)

print(num1 // num2)
print(num1 ** num2)
